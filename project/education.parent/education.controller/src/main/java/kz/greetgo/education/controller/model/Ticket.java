public class Ticket{
    public int IDTicket;
    public str Docname;
    public int Time;
}
public Ticket(int getIDTicket, str getDocname, int getTime){
    IDTicket = getIDTicket;
    Docname = getDocname;
    Time = getTime;
}
public void setIDTicket(int newIDTicket){
    IDTicket = newIDTicket;
}
public void setDocname(str newDocname){
        Docname = newDocname;
    }
public void setTime(int newTime){
    Time = newTime;
}